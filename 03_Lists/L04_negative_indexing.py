# ==========================================================
# Question L04: Access Elements Using Negative Indexing
# ----------------------------------------------------------
# Write a Python program that creates a list containing
# five colors.
#
# Display:
#
# • Complete List
# • Last element
# • Second last element
# • Third last element
#
# Example Output:
#
# Colors             : ['Red', 'Blue', 'Green', 'Yellow', 'Black']
#
# Last Color         : Black
# Second Last Color  : Yellow
# Third Last Color   : Green
# ==========================================================

colors = ["Red", "Blue", "Green", "Yellow", "Black"]

print("Colors             :", colors)
print("Last Color         :", colors[-1])
print("Second Last Color  :", colors[-2])
print("Third Last Color   :", colors[-3])