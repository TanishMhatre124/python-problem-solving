# Question L03: Access Elements of a List
# ----------------------------------------------------------
# Write a Python program that creates a list containing
# five cities.
#
# Display:
#
# • Complete List
# • First element
# • Third element
# • Last element
#
# Example Output:
#
# Cities      : ['Mumbai', 'Delhi', 'Pune', 'Bengaluru', 'Chennai']
#
# First City  : Mumbai
# Third City  : Pune
# Last City   : Chennai
# ==========================================================

Cities=['Mumbai', 'Delhi', 'Pune', 'Bengaluru', 'Chennai']
print("First City :",Cities[0])
print("second City :",Cities[1])
print("third City :",Cities[2])
print("last City :",Cities[-1])
