# Write a Python program that asks the user to enter
# a string.
#
# Display the following characters using positive indexing:
#
# • First Character
# • Second Character
# • Third Character
#
# Note:
# Positive indexing starts from 0.
#
# Example:
#
# Enter a String: Python
#
# -------------------------
# Entered String  : Python
# First Character : P
# Second Character: y
# Third Character : t
# -------------------------
#
# Hint:
# Use indexes:
#
# string[0]
# string[1]
# string[2]
# ==========================================================


input=input("enter the string :")
print("first character :",input[0])
print("second character :",input[1])
print("third character :",input[2])

