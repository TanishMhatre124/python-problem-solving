# Write a Python program that asks the user to enter
# a string.
#
# Convert the entered string into title case.
#
# In title case, the first letter of each word
# should be capitalized.
#
# Display:
#
# • Original String
# • Title Case String
#
# Example:
#
# Enter a String: hello python programming
#
# -------------------------
# Original String   : hello python programming
# Title Case String : Hello Python Programming
# -------------------------
# ==========================================================


n1=input("enter the string:")

print("original string is :",n1)
print(" title case STRING IS :",n1.title())
