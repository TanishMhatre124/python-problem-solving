# Write a Python program that asks the user to enter
# a distance in kilometers.
#
# Convert the distance into meters using the formula:
#
# Meters = Kilometers × 1000
#
# Display:
#
# • Distance in Kilometers
# • Distance in Meters
#
# Example:
#
# Enter Distance in Kilometers: 5
#
# -------------------------
# Distance in Kilometers : 5
# Distance in Meters     : 5000
# ==========================================================

kilometer=float(input("enter the distance in kilometer :"))
meter= kilometer *1000
print("distance in km :",kilometer)
print("distance in meter :", meter)